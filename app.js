function openPopup() {
    document.getElementById('sharebox').style.display = "block";
    document.getElementById('popupbtn').style.backgroundColor = "#6c7f99";
    document.getElementById('share').style.color = "#fff"
}
function closePopup() {
    document.getElementById('sharebox').style.display = "none";
    document.getElementById('popupbtn').style.backgroundColor = "#ecf1f7";
}